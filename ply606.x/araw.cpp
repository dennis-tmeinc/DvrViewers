int CARaw::OpenDecoder()
{
    switch( m_fmtIn.i_codec )
    {
    /* from wav/avi/asf file */
    case TME_FOURCC('a','r','a','w'):
    case TME_FOURCC('p','c','m',' '):
    case TME_FOURCC('a','f','l','t'):
    /* _signed_ big endian samples (mov)*/
    case TME_FOURCC('t','w','o','s'):
    /* _signed_ little endian samples (mov)*/
    case TME_FOURCC('s','o','w','t'):

    case TME_FOURCC('a','l','a','w'):
    case TME_FOURCC('u','l','a','w'):
    case TME_FOURCC('m','l','a','w'):

    case TME_FOURCC('f','l','6','4'):
    case TME_FOURCC('f','l','3','2'):
    case TME_FOURCC('s','3','2','l'):
    case TME_FOURCC('s','3','2','b'):
    case TME_FOURCC('s','2','4','l'):
    case TME_FOURCC('s','2','4','b'):
    case TME_FOURCC('s','1','6','l'):
    case TME_FOURCC('s','1','6','b'):
    case TME_FOURCC('s','8',' ',' '):
    case TME_FOURCC('u','8',' ',' '):
        break;

    default:
        return EGENERIC;
    }

    if( m_fmtIn.audio.i_channels <= 0 ||
        m_fmtIn.audio.i_channels > 8 )
    {
        _RPT1(_CRT_WARN, "bad channels count (1-8): %i\n",
                 m_fmtIn.audio.i_channels );
        return EGENERIC;
    }

    if( m_fmtIn.audio.i_rate <= 0 )
    {
        _RPT0(_CRT_WARN, "bad samplerate\n" );
        return EGENERIC;
    }

    m_pLogtos16 = NULL;

    _RPT3(_CRT_WARN, "samplerate:%dHz channels:%d bits/sample:%d\n",
             m_fmtIn.audio.i_rate, m_fmtIn.audio.i_channels,
             m_fmtIn.audio.i_bitspersample );

    if( m_fmtIn.i_codec == TME_FOURCC( 'f', 'l', '6', '4' ) )
    {
        m_fmtOut.i_codec = m_fmtIn.i_codec;
        m_fmtIn.audio.i_bitspersample = 64;
    }
    else if( m_fmtIn.i_codec == TME_FOURCC( 'f', 'l', '3', '2' ) )
    {
        m_fmtOut.i_codec = m_fmtIn.i_codec;
        m_fmtIn.audio.i_bitspersample = 32;
    }
    else if( m_fmtIn.i_codec == TME_FOURCC( 's', '3', '2', 'l' ) ||
             m_fmtIn.i_codec == TME_FOURCC( 's', '3', '2', 'b' ) )
    {
        m_fmtOut.i_codec = m_fmtIn.i_codec;
        m_fmtIn.audio.i_bitspersample = 32;
    }
    else if( m_fmtIn.i_codec == TME_FOURCC( 's', '2', '4', 'l' ) ||
             m_fmtIn.i_codec == TME_FOURCC( 's', '2', '4', 'b' ) )
    {
        m_fmtOut.i_codec = m_fmtIn.i_codec;
        m_fmtIn.audio.i_bitspersample = 24;
    }
    else if( m_fmtIn.i_codec == TME_FOURCC( 's', '1', '6', 'l' ) ||
             m_fmtIn.i_codec == TME_FOURCC( 's', '1', '6', 'b' ) )
    {
        m_fmtOut.i_codec = m_fmtIn.i_codec;
        m_fmtIn.audio.i_bitspersample = 16;
    }
    else if( m_fmtIn.i_codec == TME_FOURCC( 's', '8', ' ', ' ' ) ||
             m_fmtIn.i_codec == TME_FOURCC( 'u', '8', ' ', ' ' ) )
    {
        m_fmtOut.i_codec = m_fmtIn.i_codec;
        m_fmtIn.audio.i_bitspersample = 8;
    }
    else if( m_fmtIn.i_codec == TME_FOURCC( 'a', 'f', 'l', 't' ) )
    {
        switch( ( m_fmtIn.audio.i_bitspersample + 7 ) / 8 )
        {
        case 4:
            m_fmtOut.i_codec = TME_FOURCC('f','l','3','2');
            break;
        case 8:
            m_fmtOut.i_codec = TME_FOURCC('f','l','6','4');
            break;
        default:
            _RPT0(_CRT_WARN, "bad parameters(bits/sample)\n" );
            return EGENERIC;
        }
    }
    else if( m_fmtIn.i_codec == TME_FOURCC( 'a', 'r', 'a', 'w' ) ||
             m_fmtIn.i_codec == TME_FOURCC( 'p', 'c', 'm', ' ' ) )
    {
        switch( ( m_fmtIn.audio.i_bitspersample + 7 ) / 8 )
        {
        case 1:
            m_fmtOut.i_codec = TME_FOURCC('u','8',' ',' ');
            break;
        case 2:
            m_fmtOut.i_codec = TME_FOURCC('s','1','6','l');
            break;
        case 3:
            m_fmtOut.i_codec = TME_FOURCC('s','2','4','l');
            break;
        case 4:
            m_fmtOut.i_codec = TME_FOURCC('s','3','2','l');
            break;
        default:
            _RPT0(_CRT_WARN, "bad parameters(bits/sample)\n" );
            return EGENERIC;
        }
    }
    else if( m_fmtIn.i_codec == TME_FOURCC( 't', 'w', 'o', 's' ) )
    {
        switch( ( m_fmtIn.audio.i_bitspersample + 7 ) / 8 )
        {
        case 1:
            m_fmtOut.i_codec = TME_FOURCC('s','8',' ',' ');
            break;
        case 2:
            m_fmtOut.i_codec = TME_FOURCC('s','1','6','b');
            break;
        case 3:
            m_fmtOut.i_codec = TME_FOURCC('s','2','4','b');
            break;
        case 4:
            m_fmtOut.i_codec = TME_FOURCC('s','3','2','b');
            break;
        default:
            _RPT0(_CRT_WARN, "bad parameters(bits/sample)\n" );
            return EGENERIC;
        }
    }
    else if( m_fmtIn.i_codec == TME_FOURCC( 's', 'o', 'w', 't' ) )
    {
        switch( ( m_fmtIn.audio.i_bitspersample + 7 ) / 8 )
        {
        case 1:
            m_fmtOut.i_codec = TME_FOURCC('s','8',' ',' ');
            break;
        case 2:
            m_fmtOut.i_codec = TME_FOURCC('s','1','6','l');
            break;
        case 3:
            m_fmtOut.i_codec = TME_FOURCC('s','2','4','l');
            break;
        case 4:
            m_fmtOut.i_codec = TME_FOURCC('s','3','2','l');
            break;
        default:
            _RPT0(_CRT_WARN, "bad parameters(bits/sample)\n" );
            return EGENERIC;
        }
    }
    else if( m_fmtIn.i_codec == TME_FOURCC( 'a', 'l', 'a', 'w' ) )
    {
        m_fmtOut.i_codec = AOUT_FMT_S16_NE;
        m_pLogtos16  = alawtos16;
        m_fmtIn.audio.i_bitspersample = 8;
    }
    else if( m_fmtIn.i_codec == TME_FOURCC( 'u', 'l', 'a', 'w' ) ||
             m_fmtIn.i_codec == TME_FOURCC( 'm', 'l', 'a', 'w' ) )
    {
        m_fmtOut.i_codec = AOUT_FMT_S16_NE;
        m_pLogtos16  = ulawtos16;
        m_fmtIn.audio.i_bitspersample = 8;
    }
    else return EGENERIC;

    /* Set output properties */
    m_fmtOut.i_cat = AUDIO_ES;
    m_fmtOut.audio.i_rate = m_fmtIn.audio.i_rate;
    m_fmtOut.audio.i_channels = m_fmtIn.audio.i_channels;
    m_fmtOut.audio.i_bitspersample = m_fmtIn.audio.i_bitspersample;
    m_fmtOut.audio.i_physical_channels =
        m_fmtOut.audio.i_original_channels =
            pi_channels_maps[m_fmtIn.audio.i_channels];
    if( m_fmtIn.audio.i_physical_channels )
        m_fmtOut.audio.i_physical_channels =
            m_fmtIn.audio.i_physical_channels;
    if( m_fmtIn.audio.i_original_channels )
        m_fmtOut.audio.i_original_channels =
            m_fmtIn.audio.i_original_channels;

    if( m_fmtIn.i_codec == TME_FOURCC( 'a', 'l', 'a', 'w' ) ||
        m_fmtIn.i_codec == TME_FOURCC( 'u', 'l', 'a', 'w' ) ||
        m_fmtIn.i_codec == TME_FOURCC( 'm', 'l', 'a', 'w' ) )
    {
        m_fmtOut.audio.i_bitspersample = 16;
    }

	/* set the divider */
    aout_DateInit( &m_endDate, m_fmtOut.audio.i_rate );
    aout_DateSet( &m_endDate, 0 );

	return StartThread(); // call this at the end of OpenDecoder()
}

/****************************************************************************
 * DecodeBlock: the whole thing
 ****************************************************************************
 * This function must be fed with whole samples (see nBlockAlign).
 * Called until this function returns NULL
 ****************************************************************************/
aout_buffer_t *CARaw::DecodeAudio( block_t **pp_block )
{
    block_t *p_block;
    aout_buffer_t *p_out;
    int i_samples;

    if( !pp_block || !*pp_block ) return NULL;

    p_block = *pp_block;

    if( p_block->i_flags & BLOCK_FLAG_DISCONTINUITY )
    {
		TRACE(_T("ch:%d, BLOCK_FLAG_DISCONTINUITY returning\n"), m_pInput->GetChannelID());
		// below doesn't help at all to reduce the silent time after pause
		//if (m_pAout) m_pAout->m_pOutput->m_bResetPlayer = true;
        block_Release( p_block );
        return NULL;
    }

	//_RPT2(_CRT_WARN, "DecodeAudio(blockpts:%I64d),enddate:%I64d\n",p_block->i_pts,aout_DateGet(&m_endDate));
	if( p_block->i_pts != 0 &&
        p_block->i_pts != aout_DateGet( &m_endDate ) )
    {
		//_RPT0(_CRT_WARN, "setting enddate to block's pts\n");
        aout_DateSet( &m_endDate, p_block->i_pts );
    }
    else if( !aout_DateGet( &m_endDate ) )
    {
        /* We've just started the stream, wait for the first PTS. */
        block_Release( p_block );
        return NULL;
    }

    /* Don't re-use the same pts twice */
    p_block->i_pts = 0;

   i_samples = p_block->i_buffer * 8 / m_fmtIn.audio.i_bitspersample /
        m_fmtIn.audio.i_channels;

    if( i_samples <= 0 )
    {
        block_Release( p_block );
        return NULL;
    } // <<--- last call with null happens here

    /* Create chunks of max 1024 samples */
    i_samples = min( i_samples, 1024 );

    p_out = AoutNewBuffer( i_samples ); /* i_samples:192 for us */
    if( p_out == NULL )
    {
        block_Release( p_block );
       return NULL;
    }

	/* give this new buffer's start/end timestamp */
    p_out->start_date = aout_DateGet( &m_endDate );
    p_out->end_date   = aout_DateIncrement( &m_endDate, i_samples );

    if( m_pLogtos16 ) /* are we using alaw/ulaw codec */
    {
        INT16 *s = (INT16*)p_out->p_buffer;
        unsigned int i;

        for( i = 0; i < p_out->i_nb_bytes / 2; i++ )
        {
            *s++ = m_pLogtos16[*p_block->p_buffer++];
            p_block->i_buffer--;
        }
    }
    else
    {
        memcpy( p_out->p_buffer, p_block->p_buffer, p_out->i_nb_bytes );
        p_block->p_buffer += p_out->i_nb_bytes;
        p_block->i_buffer -= (int)p_out->i_nb_bytes;
    }

#ifdef AUDIO_DECODE_TEST
		if (m_fp) {
			fwrite(p_out->p_buffer, 1, p_out->i_nb_bytes, m_fp);
		}
#endif
	//_RPT4(_CRT_WARN, "  out->start:%I64d,end:%I64d(duration:%I64d,samples:%d)\n", p_out->start_date,p_out->end_date,p_out->end_date-p_out->start_date,i_samples);
    return p_out;
}

