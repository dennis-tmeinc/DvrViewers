/*****************************************************************************
 * EncoderOpen:
 *****************************************************************************/
static int EncoderOpen( vlc_object_t *p_this )
{
    encoder_t *p_enc = (encoder_t *)p_this;
    encoder_sys_t *p_sys;

    if( p_enc->fmt_out.i_codec == TME_FOURCC('u','8',' ',' ') ||
        p_enc->fmt_out.i_codec == TME_FOURCC('s','8',' ',' ') ||
        p_enc->fmt_out.i_codec == TME_FOURCC('a','l','a','w') ||
        p_enc->fmt_out.i_codec == TME_FOURCC('u','l','a','w') ||
        p_enc->fmt_out.i_codec == TME_FOURCC('m','l','a','w'))
    {
        p_enc->fmt_out.audio.i_bitspersample = 8;
    }
    else if( p_enc->fmt_out.i_codec == TME_FOURCC('u','1','6','l') ||
             p_enc->fmt_out.i_codec == TME_FOURCC('u','1','6','b') ||
             p_enc->fmt_out.i_codec == TME_FOURCC('s','1','6','l') ||
             p_enc->fmt_out.i_codec == TME_FOURCC('s','1','6','b') )
    {
        p_enc->fmt_out.audio.i_bitspersample = 16;
    }
    else if( p_enc->fmt_out.i_codec == TME_FOURCC('u','2','4','l') ||
             p_enc->fmt_out.i_codec == TME_FOURCC('u','2','4','b') ||
             p_enc->fmt_out.i_codec == TME_FOURCC('s','2','4','l') ||
             p_enc->fmt_out.i_codec == TME_FOURCC('s','2','4','b') )
    {
        p_enc->fmt_out.audio.i_bitspersample = 24;
    }
    else if( p_enc->fmt_out.i_codec == TME_FOURCC('u','3','2','l') ||
             p_enc->fmt_out.i_codec == TME_FOURCC('u','3','2','b') ||
             p_enc->fmt_out.i_codec == TME_FOURCC('s','3','2','l') ||
             p_enc->fmt_out.i_codec == TME_FOURCC('s','3','2','b') ||
             p_enc->fmt_out.i_codec == TME_FOURCC('f','i','3','2') ||
             p_enc->fmt_out.i_codec == TME_FOURCC('f','l','3','2') )
    {
        p_enc->fmt_out.audio.i_bitspersample = 32;
    }
    else if( p_enc->fmt_out.i_codec == TME_FOURCC('f','l','6','4') )
    {
        p_enc->fmt_out.audio.i_bitspersample = 64;
    }
    else
    {
        return EGENERIC;
    }

    /* Allocate the memory needed to store the encoder's structure */
    if( ( p_enc->p_sys = p_sys =
          (encoder_sys_t *)malloc(sizeof(encoder_sys_t)) ) == NULL )
    {
        msg_Err( p_enc, "out of memory" );
        return EGENERIC;
    }

    p_enc->pf_encode_audio = EncoderEncode;
    p_enc->fmt_in.i_codec = p_enc->fmt_out.i_codec;
    p_sys->i_s16tolog = 0;

    if( p_enc->fmt_out.i_codec == TME_FOURCC( 'a', 'l', 'a', 'w' ))
    {
        p_enc->fmt_in.audio.i_bitspersample = 16;
        p_enc->fmt_in.i_codec = AOUT_FMT_S16_NE;
        p_sys->i_s16tolog = ALAW;
    }
    else if( p_enc->fmt_out.i_codec == TME_FOURCC( 'u', 'l', 'a', 'w' ) ||
             p_enc->fmt_out.i_codec == TME_FOURCC( 'm', 'l', 'a', 'w' ) )
    {
        p_enc->fmt_in.audio.i_bitspersample = 16;
        p_enc->fmt_in.i_codec = AOUT_FMT_S16_NE;
        p_sys->i_s16tolog = ULAW;
    }

    msg_Dbg( p_enc, "samplerate:%dHz channels:%d bits/sample:%d",
             p_enc->fmt_out.audio.i_rate, p_enc->fmt_out.audio.i_channels,
             p_enc->fmt_out.audio.i_bitspersample );

    return SUCCESS;
}

